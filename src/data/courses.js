// Helper functions
export const getCourseById = (id) => courses.find((c) => c.id === id)

export const getCoursesByCategory = (categoryId) =>
  courses.filter((c) => c.category === categoryId)

export const getSessionsByCourse = (courseId) =>
  upcomingSessions.filter((s) => s.courseId === courseId)

export const getUpcomingWebinars = () =>
  upcomingSessions
    .filter((s) => new Date(s.date) > new Date())
    .sort((a, b) => new Date(a.date) - new Date(b.date))

